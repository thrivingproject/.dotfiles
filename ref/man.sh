# Basic Navigation
Enter          # Move down one line
Space          # Move down one page
b              # Move up one page
Up Arrow       # Move up one line
Down Arrow     # Move down one line
Left Arrow     # Move left one column
Right Arrow    # Move right one column

# Searching
/keyword       # Search for 'keyword' from current position (press 'n' for next, 'N' for previous match)
?keyword       # Search for 'keyword' backwards

# Exiting
q              # Quit the man page and return to the command prompt

# Other
h              # Display help for man page navigation
g              # Go to the first line of the file
G              # Go to the last line of the file
n              # Repeat the last search (forward)
N              # Repeat the last search (backward)
